int1 = 6
int2 = 13

float1 = 4.6
float2 = 9.8

# Addition
print("Sum of two int:", int1 + int2)
print(f"Sum of {int1} and {int2} is: {int1 + int2}\n\n")

# Subtraction
print(f"Float Subtraction btw {float2} and {float1} is: {(float2 - float1):.3f}")
print(f"Integer Subtraction btw {int2} and {int1} is: {int2 - int1}\n\n")

# Multiplication
print(f"Integer Multiplication of {int1} and {int2} is: {int1 * int2}.")
print(f"Float Multiplication of {float1} and {float2} is: {float1 * float2}.\n\n")

# Division
print(f"Integer Division btw {int2} and {int1} is: {(int2 / int1):.3f}")
print(f"Float Division btw {float2} and {float1} is: {(float2 / float1):.3f}")

# Modulus
print(f"Modulus value btw {int2} and {int1} is: {int2 % int1}")
print(f"2 power 4 is {2 ** 4}\n\n")

# ---- Comparison ---
print(f"Is {int1} equals to {int2}: {int1 == int2}")
print(f"Is {int1} not equals to {int2}: {int1 != int2}")
print(f"Is {int2} greater than {int1}: {int2 > int1}")
print(f"Is {int1} less than {int2}: {int1 < int2}")
print(f"Is {int2} greater than or equals to {int1}: {int2 >= int1}")
print(f"Is {int2} less than or equals to {int1}: {int2 <= int1}")

